		<div class="aba-conteudo">
			<p>A diferença entre um e outro se dá pela estrutura. Quando tratamos de um projeto de BLOG, este possui uma estrutura mais fechada para toda navegação do site. Esta estrutura fica bem com aquela carinha de blog, dividido em uma coluna de posts e outra menor lado com diversas informações (chamadas de widgets). Esta estrutura é usada em todas as páginas desde a página inicial.</p>
